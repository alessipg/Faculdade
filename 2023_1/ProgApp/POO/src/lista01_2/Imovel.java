package lista01_2;

public class Imovel {
	private String endereco;
	private double valor;
	
	public Imovel(String endereco, double valor) {
		this.endereco = endereco;
		this.valor = valor;
	}

	public String getEndereco() {
		return endereco;
	}

	public double getValor() {
		return valor;
	}
	
	
}

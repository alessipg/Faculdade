package lista03_1;

public class Sala {
	
	private int idSala;
	private int capacidadeMax;
	
	
	public Sala(int idSala, int capacidadeMax) {
		this.idSala = idSala;
		this.capacidadeMax = capacidadeMax;
	}


	public int getIdSala() {
		return idSala;
	}


	public int getCapacidadeMax() {
		return capacidadeMax;
	}
	
	
	
	
}

package aula03;

public class Exemplo01 {
	public static void main (String [] args) {
		boolean completo = true;
		char letra = 'A';//aspas simples
		byte valorByte = 126;
		int valorInt = 1000;
		long valorLong = 999999999;
		float valorFloat = 4.5f; //f indica que a variavel é float
		double valorDouble = 4.5; //para double não há necessidade
		
		System.out.println(completo);
		System.out.println(letra);
		System.out.println(valorByte);
		System.out.println(valorInt);
		System.out.println(valorLong);
		System.out.println(valorFloat);
		System.out.println(valorDouble);
		
	}
}
